# 길이 폭으로 넓이를 구하는 프로그램
# 사용자로부터 길이, 폭을 입력


# length 길이
# width 폭


# length* width

#출력 상태
# 넓이 : **

length = int(input("길이를 입력하세요>"))
width = int(input("폭을 입력하세요>"))

answer = length*width
print("넓이 : ",answer)

